            <h1 id="The-best-years">2008-20. The best years in our history</h1>

        </div>

        <div class="article">


            <p>Barça delight the world with their brand of football that is appreciated by football fans from all over
                the world. They win three more Champions League titles in a decade of dominance for the blaugranes</p>
            <p>From the season 2008/09 under Josep Guardiola, the team improved even further. Playing with the same
                style that Cruyff had introduced, Guardiola was a firm supporter of basing his team around the club's
                own youth system and promoted several young talents to the first team, and the result was the greatest
                Barça team ever.</p>
            <p>The greatest international recognition of this came when the FIFA Ballon d'Or nominations in 2010
                shortlisted Xavi, Iniesta and Messi for the honour, all three of whom had grown up at La Masia, the
                residence where young sportspeople of all ages are trained and educated.</p>
            <p>This team was the culmination of everything that FC Barcelona stands for, and produced an amazing string
                of major titles, including two Champions Leagues and three Spanish Leagues, plus the long-awaited Clubs
                World Cup, which was finally won in 2009, that extraordinary year when Barcelona won all six major
                trophies, something unprecedented in the history of European football. Barça broke all kinds of records,
                played memorable matches and won just about every title on offer. But the finest hour for Guardiola's
                side came at Wembley on 28 May, 2011, when the world was enchanted by the kind of football that dreams
                are made of. The world's press bestowed praise on this extraordinary side that had written one of the
                most incredible chapters in the history of the game. The Guardiola era ended in 2012 but Barça continued
                their success under Tito Vilanova (the league in 2012/13) and Luis Enrique who in 2015 won the Champions
                League, the League and the Copa del Rey in another historic treble winning season.</p>
            <p>But it wasn't just the football team that was collecting honours. The basketball, handball, futsal and
                roller hockey teams also won further European titles. Furthermore, the season 2011/12 and 2014/15 ended
                with a new record by winning 17 professional titles. Never before had FC Barcelona won so much
                silverware in a single season during its 118 year history.</p>

        </div>


        <div class="title">
            <h1 id="The-best-years">THE RETURN OF GUARDIOLA </h1>
        </div>
        <div class="article2">
            <p>In 2008, Josep Guardiola accepted the difficult job of replacing Frank Rijkaard as coach and end a run of
                two seasons without a trophy. To do so, he applied the same style of play that was used when he was a
                player at the Club, an attacking philosophy based on passing and it turned out to be a great success.
            </p>
            <p>In his debut season, Guardiola won all three trophies available. After a winning start to the following
                campaign in 2009/10, Barça became the team of the Six Trophies, all won in the calendar year of 2009.
                The run of success continued with Guardiola claiming 14 titles of a possible 19 as coach in his four
                seasons in charge. Guardiola became a reference point in football and his Barça side went beyond the
                strictly footballing field.</p>

        </div>


        <div class="title">
            <h1 id="The-best-years">BARÇA WIN THEIR THIRD CHAMPIONS LEAGUE </h1>
        </div>

        <div class="article3">
            <p>FC Barcelona went to the Olympic Stadium looking to claim their third title of the season after having
                won the Copa del Rey and the league. Guardiola went into the final against Manchester United with
                several players missing in defence. The Premier League side started strongly but Eto’o’s goal changed
                the game and from then on Barça dominated with Messi adding a second to make the final score 2-0. On 27
                May 2009 Barça claimed their third Champions League title thanks to victory over the holders, becoming
                the first Spanish side to claim the treble of the Copa del Rey, the league and the Champions League.</p>

        </div>






        
